    public void showData(List<ChiTietSPCustom> qlct) {
        model.setRowCount(0);
        for (ChiTietSPCustom qLChiTietSP : qlct) {
            model.addRow(qLChiTietSP.toRowData1());
        }
    }

    public void fillDataToTable(int index) {
        ChiTietSPCustom qlctsp = listQlCTSanPham.get(index);
        txtNamBh.setText(qlctsp.getNamBH() + "");
        txtMota.setText(qlctsp.getMoTa() + "");
        txtSlTon.setText(qlctsp.getSoLuongTon() + "");
        txtGiaNhap.setText(qlctsp.getGiaNhap() + "");
    }

    public ChiTietSPCustom checkValidate() {
        String nam = txtNamBh.getText();
        String moTa = txtMota.getText();
        String sLTon = txtSlTon.getText();
        String giaNhap = txtGiaNhap.getText();
        if (nam.isEmpty() || moTa.isEmpty() || sLTon.isEmpty() || giaNhap.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng không để trống bất kì trường nào");
            return null;
        }
        ChiTietSPCustom qlctsp = new ChiTietSPCustom();
        qlctsp.setNamBH(Integer.parseInt(nam));
        qlctsp.setMoTa(moTa);
        qlctsp.setSoLuongTon(Integer.parseInt(sLTon));
        qlctsp.setGiaNhap(BigDecimal.valueOf(Double.parseDouble(giaNhap)));
        return qlctsp;

    }

