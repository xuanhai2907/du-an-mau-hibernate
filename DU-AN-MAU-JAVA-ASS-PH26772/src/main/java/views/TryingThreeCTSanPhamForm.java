    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        ChiTietSPCustom ql = checkValidate();
        if (ql == null) {
            return;
        }
        chiTietSanPhamService.them(ql);
        showData(chiTietSanPhamService.getList());
        JOptionPane.showMessageDialog(this, "Thêm thành công");
        clearForm();
    }//GEN-LAST:event_themActionPerformed

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        ChiTietSPCustom ql = checkValidate();
        if (ql == null) {
            return;
        }
        int s = tbCTSanPham.getSelectedRow();
        if (s == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 sản phẩm để sửa");
            return;
        }
        ChiTietSPCustom qlctsp = listQlCTSanPham.get(s);
        ql.setId(qlctsp.getId());
        chiTietSanPhamService.sua(ql);

        showData(chiTietSanPhamService.getList());
        JOptionPane.showMessageDialog(this, "Sửa thành công");
        clearForm();
    }//GEN-LAST:event_suaActionPerformed

    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        int s = tbCTSanPham.getSelectedRow();
        if (s == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 sản phẩm để xóa");
            return;
        }
        chiTietSanPhamService.xoa(listQlCTSanPham.get(s).getId());
        showData(chiTietSanPhamService.getList());
        JOptionPane.showMessageDialog(this, "Xóa thành công");
        clearForm();
    }//GEN-LAST:event_xoaActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String namBH = cbbSearch.getSelectedItem().toString();
        System.out.println(namBH);
        int TestTreatmentID = Integer.parseInt(namBH);
        listQlCTSanPham = chiTietSanPhamService.getListByNamBH(TestTreatmentID);
        if (listQlCTSanPham.size() == 0) {
            showData(chiTietSanPhamService.getList());
        } else {
            showData(listQlCTSanPham);
        }
    }//GEN-LAST:event_searchActionPerformed

