    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        if (nhanVienService.checkMa(txtMa.getText())) {
            JOptionPane.showMessageDialog(this, "Mã trùng");
            return;
        }

        NhanVienCustom qlnv = checkValidate();
        if (qlnv == null) {
            return;
        }
        
        nhanVienService.them(qlnv);
        listQLNhanVien = nhanVienService.getList();;
        showData(listQLNhanVien);
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_themActionPerformed

    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed

        NhanVienCustom qlnv = checkValidate();
        if (qlnv == null) {
            return;
        }
        int s = tbNhanVien.getSelectedRow();
        if (s == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập 1 dòng để sửa");
            return;
        }
        NhanVienCustom ql = listQLNhanVien.get(s);
        qlnv.setId(ql.getId());
        qlnv.setMa(ql.getMa());
        nhanVienService.sua(qlnv);
        listQLNhanVien = nhanVienService.getList();;
        showData(listQLNhanVien);
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_suaActionPerformed

    private void xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaActionPerformed
        int s = tbNhanVien.getSelectedRow();
        if (s == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 dòng để xóa");
            return;
        }
        nhanVienService.xoa(listQLNhanVien.get(s).getId());
        listQLNhanVien.remove(s);
        listQLNhanVien = nhanVienService.getList();
        showData(listQLNhanVien);
        JOptionPane.showMessageDialog(this, "Xóa thành công");
    }//GEN-LAST:event_xoaActionPerformed

