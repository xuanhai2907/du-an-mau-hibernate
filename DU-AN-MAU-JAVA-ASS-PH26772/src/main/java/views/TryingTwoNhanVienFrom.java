    public void showData(List<NhanVienCustom> qlnv) {
        model.setRowCount(0);
        for (NhanVienCustom qLNhanVien : qlnv) {
            model.addRow(qLNhanVien.toRowData1());
        }
    }

    public void fillDataToTable(int index) {
        NhanVienCustom qlnv = listQLNhanVien.get(index);
        txtMa.setText(qlnv.getMa());
        txtTen.setText(qlnv.getHo() + " " + qlnv.getTenDem() + " " + qlnv.getTen());
        txtSdt.setText(qlnv.getSdt());
        txtDiaChi.setText(qlnv.getDiaChi());
    }

    public NhanVienCustom checkValidate() {
        NhanVienCustom qlnv = new NhanVienCustom();
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        String sdt = txtSdt.getText().trim();
        String diaChi = txtDiaChi.getText().trim();
        if (ma.equals("")) {
            JOptionPane.showMessageDialog(this, "Mã không được trống");
            return null;
        }
        if (ten.equals("")) {
            JOptionPane.showMessageDialog(this, "Tên không được trống");
            return null;
        }
        if (sdt.equals("")) {
            JOptionPane.showMessageDialog(this, "SDT không được trống");
            return null;
        }
        if (!sdt.matches("0+\\d{9}")) {
            JOptionPane.showMessageDialog(this, "Sai định dạng sdt 0 + 9 số");
            return null;
        }
        if (diaChi.equals("")) {
            JOptionPane.showMessageDialog(this, "Địa chỉ không được trống");
            return null;
        }
        int cuoi = ten.lastIndexOf(" ");
        int dau = ten.indexOf(" ");
        String tenNew = ten.substring(cuoi, ten.length());
        String tenDem = ten.substring(dau, cuoi);
        String ho = ten.substring(0, dau);

        qlnv.setTen(tenNew);
        qlnv.setTenDem(tenDem);
        qlnv.setHo(ho);
        qlnv.setMa(ma);
        qlnv.setSdt(sdt);
        qlnv.setDiaChi(diaChi);
        return qlnv;
    }

