    public void showData(List<NSX> list) {
        dtm.setRowCount(0);
        for (NSX nsx : list) {
            dtm.addRow(nsx.toRowData());
        }
    }

    public void fillData(int i) {
        NSX nsx = listNSX.get(i);
        lblID.setText(String.valueOf(nsx.getId()));
        txtMa.setText(nsx.getMa());
        txtTen.setText(nsx.getTen());
    }

