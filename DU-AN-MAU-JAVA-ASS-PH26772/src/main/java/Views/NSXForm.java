    private NSX checkValidate() {
        String ten = txtTen.getText();
        String ma = txtMa.getText();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ma khong duoc de trong");
            return null;
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ten khong duoc de trong");
            return null;
        }
        NSX nsx = new NSX(ma, ten);
        return nsx;
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        NSX nsx = checkValidateSua();
        if (nsx == null) {
            return;
        }
        JOptionPane.showMessageDialog(this, service.sua(nsx));

        listNSX = service.getAll();
        showData(listNSX);
    }//GEN-LAST:event_btnUpdateActionPerformed
    private NSX checkValidateSua() {
        String id = lblID.getText();
        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "chọn một cái để sửa");
            return null;
        }
        String ma = txtMa.getText();
        if (ma.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ma không được để trống");
            return null;
        }
        String ten = txtTen.getText();

        if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ten khong duoc de trong");
            return null;
        }

        NSX nsx = new NSX(UUID.fromString(id), ma, ten);
        return nsx;
    }
